public class UmACem {
    public static void main(String[] args) {

        int numero = 1;
        while (numero <= 100) {
            System.out.println("contagem while: " + numero);
            numero++;
        }
        for (int num = 1; num <= 100 ;num++)
            System.out.println("contagem for: " + num);

    }
}
